export const TOTAL_TOPICS = MODULES.reduce((sum, m) => sum + m.topics.length, 0);

export function getModuleById(id: string): ModuleMeta | undefined {
  return MODULES.find(m => m.id === id);
}

export function getTopicByIds(moduleId: string, topicIndex: number): TopicMeta | undefined {
  const mod = getModuleById(moduleId);
  return mod?.topics[topicIndex];
}

export function getAllTopics(): (TopicMeta & { moduleId: string; moduleTitle: string })[] {
  return MODULES.flatMap(m =>
    m.topics.map(t => ({ ...t, moduleId: m.id, moduleTitle: m.title }))
  );
}
